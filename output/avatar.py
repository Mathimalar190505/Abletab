def show_sign_language(text):
    """
    Simulates displaying a sign language avatar for the given text.
    In Phase 1, this prints a message indicating what the avatar would sign.

    Args:
        text (str): The text the avatar should sign.
    """
    print("\n[AVATAR] 🤟 Showing Sign Language Avatar...")
    print(f'  (Avatar signing) → "{text}"')
    print("[AVATAR] Animation complete.")
